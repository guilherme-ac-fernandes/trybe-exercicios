const a = 6;
const b = 4;

// Realizando Operações Aritméticas Básicas

// Soma:
const soma = a + b;
console.log(soma);

// Subtração:
const subtracao = a - b;
console.log(subtracao);

// Multiplicação:
const multiplicacao = a * b;
console.log(multiplicacao);

// Divisão:
const divisao = a / b;
console.log(divisao);

// Módulo ou Resto:
const modulo = a % b;
console.log(modulo);
