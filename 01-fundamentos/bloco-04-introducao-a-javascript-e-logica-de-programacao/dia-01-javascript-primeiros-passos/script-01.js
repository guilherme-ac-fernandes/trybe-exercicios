const a = 6;
const b = 4;

// Realizando Operações Aritméticas Básicas

console.log("Primeiro fator =", a);
console.log("Segundo fator =", b);

// Soma:
const soma = a + b;
console.log("Soma dos fatores =", soma);

// Subtração:
const subtracao = a - b;
console.log("Subtração dos fatores =", subtracao);

// Multiplicação:
const multiplicacao = a * b;
console.log("Multiplicação dos fatores =", multiplicacao);

// Divisão:
const divisao = a / b;
console.log("Divisão dos fatores =", divisao);

// Módulo ou Resto:
const modulo = a % b;
console.log("Módulo ou resto dos fatores =", modulo);
